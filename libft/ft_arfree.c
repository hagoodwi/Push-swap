#include "libft.h"

char	**ft_arfree(char ***s)
{
	int i;

	i = 0;
	while ((*s)[i] != NULL)
	{
		free((*s)[i]);
		i++;
	}
	free(*s);
	*s = NULL;
	return (NULL);
}
